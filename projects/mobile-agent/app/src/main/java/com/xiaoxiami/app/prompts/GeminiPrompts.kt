【行动指导生成规则 - 重点优化】

1. **任务难度**：根据事项复杂度判断
   - 简单：<30分钟，如打电话、发邮件、查资料
   - 中等：30分钟-2小时，如写报告、做PPT、整理文档
   - 复杂：>2小时，如项目策划、深度研究、系统开发

2. **预计耗时**：
   - 基于任务性质合理估算，同时填入第5个字段
   - 确保各步骤时间相加等于总时间（±5分钟误差可接受）

3. **最佳时段**：
   - 早上：深度思考、创作、重要决策类任务
   - 下午：沟通、会议、执行类任务
   - 晚上：学习、总结、规划类任务
   - 可留空：无明显时段偏好的任务

4. ⭐ **行动步骤（核心优化）**：
   
   **步骤数量要求：**
   - 简单任务（<30分钟）：5-8个步骤
   - 中等任务（30分钟-2小时）：8-12个步骤
   - 复杂任务（>2小时）：12-15个步骤
   
   **步骤粒度要求：**
   - 每步控制在1-10分钟内
   - 用户无需思考"接下来干什么"，直接照做即可
   - 步骤格式：【具体动作描述(预计时间)】
   
   **步骤描述要求（必须遵守）：**
   ✅ 使用动词开头：打开、点击、输入、选择、确认、拨打、发送、检查、保存等
   ✅ 明确操作对象：具体的按钮名称、菜单位置、输入框字段、文件名等
   ✅ 说明操作目的：让用户知道为什么要做这一步
   ✅ 包含判断标准：如何知道这一步完成了
   ❌ 避免模糊词汇：不要用"准备"、"整理"、"处理"等笼统词汇
   ❌ 避免跳跃逻辑：不要省略中间步骤
   
   **示例对比：**
   
   ❌ 差："准备材料(10分钟)"
   ✅ 好："打开电脑上的'项目文件夹'，新建名为'汇报材料_日期'的Word文档，将上周的三份数据表（销售、成本、利润）复制粘贴进去(10分钟)"
   
   ❌ 差："发送邮件(5分钟)"
   ✅ 好："点击邮箱右上角'撰写'按钮，收件人填写zhangsan@company.com，抄送lisi@company.com，主题写'【项目进展】XX项目汇报'，正文粘贴刚才准备的内容，点击发送(5分钟)"
   
   ❌ 差："联系客户(15分钟)"
   ✅ 好："打开微信，搜索'张总'，发送消息：'张总您好，关于XX合同有3个细节想和您确认，您现在方便电话沟通10分钟吗？'，等待回复(3分钟)；收到同意后拨打电话，按照备忘录的3个要点逐一确认(10分钟)；通话结束后发送微信：'感谢您的时间，稍后我会发邮件总结今天确认的内容'(2分钟)"

5. ⭐ **执行建议（优化）**：
   
   **数量要求：** 3-5条
   
   **类型要求（必须包含以下至少3种）：**
   - 💡 效率技巧：如何更快完成任务
   - 📋 工具推荐：有哪些辅助工具或模板
   - ⚡ 快捷方式：键盘快捷键、软件技巧等
   - 🎯 质量保证：如何避免常见错误、确保准确性
   - ⏰ 时间管理：最佳执行时段、如何拆分时间
   - 🗣️ 沟通话术：如果涉及沟通，提供具体话术模板
   
   **格式要求：**
   - 使用emoji前缀分类
   - 每条建议要具体可操作，不要泛泛而谈

6. **相关背景**：
   - 从记忆内容中提取与该待办相关的背景信息
   - 包含：人物、时间、地点、事件起因、关键细节等
   - 帮助用户快速回忆上下文

